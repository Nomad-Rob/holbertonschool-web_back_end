// This function calculates the sum of initialNumber, expansion1989, and expansion2019.
// It provides default values for expansion1989 and expansion2019 if not provided.
export default function getSumOfHoods(initialNumber, expansion1989 = 89, expansion2019 = 19) {
  // Calculate the sum by adding the provided or default values.
  return initialNumber + expansion1989 + expansion2019;
}
